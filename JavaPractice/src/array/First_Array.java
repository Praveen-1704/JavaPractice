package array;

import java.util.Arrays;

public class First_Array {
	
	
	
	public static void main(String[] args) {
		
		int arr[] = {2,4,7,0,4};
		
		System.out.println(arr[2]);
		
		System.out.println("======================");
		
		for(int i=0;i<arr.length;i++)
		{
			System.out.println(arr[i]);
		}
		
		System.out.println("======================");
		
		System.out.println(Arrays.toString(arr));
		
		
	}

}
